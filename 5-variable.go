package main

import "fmt"

func main() {
	var name string

	name = "Fanny Arif"
	fmt.Println(name)

	name = "Fanny Nasrudin"
	fmt.Println(name)

	var girlFriend = "Farda Ayu"
	fmt.Println(girlFriend)

	fullName := "Fanny Arif Nasrudin"
	fmt.Println(fullName)

	//multiple variabel

	var (
		firstName = "Fanny"
		lastName  = "Nasrudin"
	)
	fmt.Println(firstName, lastName)

}
